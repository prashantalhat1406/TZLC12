package com.kfandra.tzlc12;



public class Club {
    private String id;
    private String clubName;
    private String clubShortName;
    private String managerName;
    private String manager2Name;
    private String homeGround;
    private String clubColor;
    private int organization;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getClubName() {
        return clubName;
    }

    public void setClubName(String clubName) {
        this.clubName = clubName;
    }

    public String getClubShortName() {
        return clubShortName;
    }

    public void setClubShortName(String clubShortName) {
        this.clubShortName = clubShortName;
    }

    public String getManagerName() {
        return managerName;
    }

    public void setManagerName(String managerName) {
        this.managerName = managerName;
    }

    public String getManager2Name() {
        return manager2Name;
    }

    public void setManager2Name(String manager2Name) {
        this.manager2Name = manager2Name;
    }

    public String getHomeGround() {
        return homeGround;
    }

    public void setHomeGround(String homeGround) {
        this.homeGround = homeGround;
    }

    public String getClubColor() {
        return clubColor;
    }

    public void setClubColor(String clubColor) {
        this.clubColor = clubColor;
    }

    public int getOrganization() {
        return organization;
    }

    public void setOrganization(int organization) {
        this.organization = organization;
    }
}
